package Interpreter;

public class Context {
    private String input;
    private int output;

    public String getInput() {
        return input;
    }

    public int getOutput() {
        return output;
    }

    public void setInput(String input) {
        this.input = input;
    }

    public void setOutput(int output) {
        this.output = output;
    }
}
